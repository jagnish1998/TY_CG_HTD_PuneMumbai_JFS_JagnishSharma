package com.capgemini.generalstudent;

public class Student 
{
	int id;
	String name;
	char Gender;
	double percentage;
	
	public Student(int id, String name, char gender, double percentage) {
		super();
		this.id = id;
		this.name = name;
		Gender = gender;
		this.percentage = percentage;
	}

	public Student() {
		super();
	}
	
	
}
