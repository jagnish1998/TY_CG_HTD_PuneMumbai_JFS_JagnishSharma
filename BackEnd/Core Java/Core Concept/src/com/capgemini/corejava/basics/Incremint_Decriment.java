package com.capgemini.corejava.basics;

public class Incremint_Decriment 
{
	public static void main(String[] args) 
	{
		int i = 10;
		int j = 20;
		System.out.println("i++ "+ i++);	// substitute > Utilise > increment(post increment)
		System.out.println("i "+i);
		System.out.println("++i "+ ++i);	// increment  > substitute > Utilise(pre increment)
		System.out.println("i " +i);
		System.out.println("j-- "+ i--);	// substitute > Utilise > decrement(post decrement)
		System.out.println("j " +j);
		System.out.println("--j "+ --i);	// decrement  > substitute > Utilise(pre decrement)
		System.out.println("j " +j);
	}
}
