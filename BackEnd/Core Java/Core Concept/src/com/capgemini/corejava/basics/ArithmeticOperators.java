package com.capgemini.corejava.basics;

public class ArithmeticOperators
{
	public static void main(String[] args) 
	{
		int i = 10;
		int j = 20;
		
		System.out.println("i+j " + (i + j)); // Addition
		System.out.println("i-j " + (i - j)); // Subtraction
		System.out.println("i*j " + (i * j)); // Multiplication
		System.out.println("i/j " + (j / i)); // Division
		System.out.println("i%j " + (j % i)); // Mod
	}
}
