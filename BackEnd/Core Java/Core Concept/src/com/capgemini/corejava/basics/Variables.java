package com.capgemini.corejava.basics;

public class Variables
{
	public static void main(String[] args) 
	{
		byte b = 10;			// Initialisation and Declaration of byte
		short s = 20;			// Initialisation and Declaration of Short
		long  l =  40l;			// Initialisation and Declaration of long
		float f = 60.4f;		// Initialisation and Declaration of float
		double  d =  46.4444;	// Initialisation and Declaration of double
		char c  = 'd';			// Initialisation and Declaration of char
		boolean q = true; 		// Initialisation and Declaration of boolean
		int a;					// Declaration 
		a =30;					// Initialisation
		System.out.println("byte "+b);  // Utilisation
		System.out.println("Short "+s);
		System.out.println("long "+l);
		System.out.println("float "+f);
		System.out.println("double "+d);
		System.out.println("Char "+c);
		System.out.println("boolean "+q);
		System.out.println("int "+a);
	}
}
